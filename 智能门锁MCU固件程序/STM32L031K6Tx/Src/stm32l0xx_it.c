/**
* @brief This function handles AES and LPUART1 interrupts / LPUART1 wake-up interrupt through EXTI line 28.
*/
void AES_RNG_LPUART1_IRQHandler(void)
{
  /* USER CODE BEGIN AES_RNG_LPUART1_IRQn 0 */
    extern uint8_t LP_UartRec;
    uint32_t timeout = 0;
  /* USER CODE END AES_RNG_LPUART1_IRQn 0 */
  HAL_UART_IRQHandler(&hlpuart1);
  /* USER CODE BEGIN AES_RNG_LPUART1_IRQn 1 */
    timeout = 0;
    while (HAL_UART_GetState(&hlpuart1) != HAL_UART_STATE_READY) //等待就绪
    {
        timeout++; ////超时处理
        if (timeout > HAL_MAX_DELAY)
            break;
    }

    timeout = 0;
    while (HAL_UART_Receive_IT(&hlpuart1, &LP_UartRec, 1) != HAL_OK) //一次处理完成之后，重新开启中断并设置RxXferCount为1
    {
        timeout++; //超时处理
        if (timeout > HAL_MAX_DELAY)
            break;
    }
  /* USER CODE END AES_RNG_LPUART1_IRQn 1 */
}

